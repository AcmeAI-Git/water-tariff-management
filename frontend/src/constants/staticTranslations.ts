/**
 * Get a static translation by language and dot-separated key path.
 * Example: getStaticTranslation('bn', 'common.logOut') => "লগ আউট"
 */
export function getStaticTranslation(
    lang: Language,
    keyPath: string
): string {
    const keys = keyPath.split(".");
    let current: string | NestedStrings | undefined = staticTranslations[lang] ?? staticTranslations.en;
    for (const k of keys) {
        if (current == null || typeof current !== "object") return keyPath;
        current = current[k] as string | NestedStrings | undefined;
    }
    return typeof current === "string" ? current : keyPath;
}
